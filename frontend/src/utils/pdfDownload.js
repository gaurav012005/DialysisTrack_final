// 3. APPOINTMENT CONFIRMATION PDF
// ─────────────────────────────────────────────────────────────────────────────
export function downloadAppointmentPDF(appointment, patient = {}) {
    const doc = new jsPDF({ unit: 'pt', format: 'a4' });
    const now = new Date().toLocaleString('en-IN', {
        day: '2-digit', month: 'short', year: 'numeric',
        hour: '2-digit', minute: '2-digit'
    });
    const aptDate = appointment.appointment_date
        ? new Date(appointment.appointment_date).toLocaleDateString('en-IN', {
            weekday: 'long', day: '2-digit', month: 'long', year: 'numeric'
          })
        : 'N/A';

    _addHeader(doc, 'APPOINTMENT CONFIRMATION', `Generated: ${now}`);

    const pageW = doc.internal.pageSize.getWidth();
    let y = 46;

    /* ── Confirmed banner ── */
    doc.setFillColor(209, 250, 229);
    doc.roundedRect(14, y, pageW - 28, 36, 5, 5, 'F');
    doc.setFont('helvetica', 'bold'); doc.setFontSize(12); doc.setTextColor(...GREEN_COLOR);
    doc.text('Appointment Confirmed', pageW / 2, y + 22, { align: 'center' });
    y += 48;

    y = _table(doc, {
        startY: y,
        body: [
            ['Patient Name',      patient.name        || 'N/A'],
            ['Patient ID',        patient.patient_id  || 'N/A'],
            ['Appointment Date',  aptDate],
            ['Shift',             ((appointment.shift || 'N/A').charAt(0).toUpperCase() + (appointment.shift || '').slice(1))],
            ['Scheduled Time',    appointment.scheduled_time  || 'N/A'],
            ['Machine',           appointment.machine_number  || 'To be assigned'],
            ['Status',            (appointment.status || 'Scheduled').replace('_', ' ').toUpperCase()],
            ['Notes',             appointment.notes || '—'],
        ],
        columnStyles: {
            0: { fontStyle: 'bold', cellWidth: 160, fillColor: LIGHT_GRAY, textColor: DARK_COLOR },
            1: { textColor: DARK_COLOR },
        },
        bodyStyles: { fontSize: 9.5, cellPadding: 9 },
        alternateRowStyles: { fillColor: WHITE },
        margin: { left: 14, right: 14 },
        tableWidth: pageW - 28,
    }) + 18;

    doc.setFontSize(8.5);
    doc.setFont('helvetica', 'italic');
    doc.setTextColor(107, 114, 128);
    doc.text(
        'Please arrive 15 minutes before your scheduled time. Bring this document for check-in.',
        14, y, { maxWidth: pageW - 28 }
    );

    _addFooter(doc);
    doc.save(`Appointment_${appointment.id || 'confirmation'}.pdf`);
}


// ─────────────────────────────────────────────────────────────────────────────
// 4. DIALYSIS PRESCRIPTION PDF
// ─────────────────────────────────────────────────────────────────────────────
export function downloadPrescriptionPDF(prescription, patient = {}) {
    const doc = new jsPDF({ unit: 'pt', format: 'a4' });
    const now = new Date().toLocaleString('en-IN', {
        day: '2-digit', month: 'short', year: 'numeric',
        hour: '2-digit', minute: '2-digit'
    });

    _addHeader(doc, 'DIALYSIS PRESCRIPTION', `Effective: ${prescription.effective_date || 'N/A'}`);

    const pageW = doc.internal.pageSize.getWidth();
    let y = 44;

    /* ── Rx Banner ── */
    doc.setFillColor(239, 246, 255);
    doc.roundedRect(14, y, pageW - 28, 32, 5, 5, 'F');
    doc.setFont('helvetica', 'bold'); doc.setFontSize(14); doc.setTextColor(30, 64, 175);
    doc.text('Rx  DIALYSIS ORDER', pageW / 2, y + 21, { align: 'center' });
    y += 44;

    /* ── Patient Info ── */
    y = _sectionLabel(doc, 'Patient Information', y);
    y = _table(doc, {
        startY: y,
        body: [
            ['Patient Name', patient.name || `${patient.first_name || ''} ${patient.last_name || ''}`.trim() || 'N/A'],
            ['Patient ID', patient.patient_id || 'N/A'],
            ['Blood Type', patient.blood_type || 'N/A'],
            ['Vascular Access', patient.vascular_access || 'N/A'],
            ['Prescribed By', prescription.prescribed_by_name || 'N/A'],
        ],
        columnStyles: {
            0: { fontStyle: 'bold', cellWidth: 160, fillColor: LIGHT_GRAY },
            1: { textColor: DARK_COLOR },
        },
        bodyStyles: { fontSize: 9, cellPadding: 7 },
        alternateRowStyles: { fillColor: WHITE },
        margin: { left: 14, right: 14 },
        tableWidth: pageW - 28,
    }) + 12;

    /* ── Session Parameters ── */
    y = _sectionLabel(doc, 'Session Parameters', y);
    y = _table(doc, {
        startY: y,
        head: [['Parameter', 'Prescribed Value']],
        body: [
            ['Session Duration', `${prescription.session_duration_minutes || 240} minutes`],
            ['Frequency', prescription.frequency_display || prescription.frequency || 'N/A'],
            ['Blood Flow Rate (Qb)', `${prescription.target_blood_flow_rate || 300} ml/min`],
            ['Dialysate Flow Rate (Qd)', `${prescription.target_dialysate_flow_rate || 500} ml/min`],
            ['Dialyzer Type', prescription.dialyzer_type_display || prescription.dialyzer_type || 'N/A'],
            ['Dialyzer Model', prescription.dialyzer_model || 'N/A'],
            ['Target Dry Weight', prescription.target_dry_weight ? `${prescription.target_dry_weight} kg` : 'N/A'],
            ['Target UF Volume', prescription.target_uf_volume ? `${prescription.target_uf_volume} L` : 'N/A'],
            ['Target Kt/V', prescription.target_ktv ? `>= ${prescription.target_ktv}` : '>= 1.2'],
        ],
        headStyles: { fillColor: BRAND_COLOR, textColor: WHITE, fontStyle: 'bold', fontSize: 9 },
        bodyStyles: { fontSize: 8.5, textColor: DARK_COLOR },
        alternateRowStyles: { fillColor: [239, 246, 255] },
        margin: { left: 14, right: 14 },
        tableWidth: pageW - 28,
        columnStyles: { 0: { fontStyle: 'bold', cellWidth: 200 } },
    }) + 12;

    /* ── Anticoagulation ── */
    y = _sectionLabel(doc, 'Anticoagulation', y);
    y = _table(doc, {
        startY: y,
        body: [
            ['Heparin Dose', prescription.heparin_dose ? `${prescription.heparin_dose} Units` : 'N/A'],
            ['Heparin Protocol', prescription.heparin_frequency || 'Loading + maintenance'],
        ],
        columnStyles: {
            0: { fontStyle: 'bold', cellWidth: 200, fillColor: LIGHT_GRAY },
        },
        bodyStyles: { fontSize: 8.5, cellPadding: 7 },
        alternateRowStyles: { fillColor: WHITE },
        margin: { left: 14, right: 14 },
        tableWidth: pageW - 28,
    }) + 12;

    /* ── Dialysate Composition ── */
    y = _sectionLabel(doc, 'Dialysate Composition', y);
    y = _table(doc, {
        startY: y,
        head: [['Electrolyte', 'Concentration']],
        body: [
            ['Sodium (Na+)', `${prescription.dialysate_sodium || 140} mEq/L`],
            ['Potassium (K+)', `${prescription.dialysate_potassium || 2.0} mEq/L`],
            ['Calcium (Ca2+)', `${prescription.dialysate_calcium || 2.5} mEq/L`],
            ['Bicarbonate (HCO3-)', `${prescription.dialysate_bicarbonate || 35} mEq/L`],
        ],
        headStyles: { fillColor: GREEN_COLOR, textColor: WHITE, fontStyle: 'bold', fontSize: 9 },
        bodyStyles: { fontSize: 8.5, textColor: DARK_COLOR },
        alternateRowStyles: { fillColor: [240, 253, 244] },
        margin: { left: 14, right: 14 },
        tableWidth: pageW - 28,
        columnStyles: { 0: { fontStyle: 'bold', cellWidth: 200 } },
    }) + 12;

    /* ── Additional Medications ── */
    const meds = [];
    if (prescription.epo_dose) meds.push(['EPO/ESA', prescription.epo_dose]);
    if (prescription.iron_dose) meds.push(['IV Iron', prescription.iron_dose]);
    if (prescription.additional_medications) meds.push(['Other', prescription.additional_medications]);
    if (meds.length > 0) {
        y = _sectionLabel(doc, 'Additional Medications During Session', y);
        y = _table(doc, {
            startY: y,
            body: meds,
            columnStyles: { 0: { fontStyle: 'bold', cellWidth: 150, fillColor: [254, 243, 199] } },
            bodyStyles: { fontSize: 8.5, cellPadding: 7 },
            margin: { left: 14, right: 14 },
            tableWidth: pageW - 28,
        }) + 12;
    }

    /* ── Special Instructions ── */
    if (prescription.special_instructions) {
        y = _sectionLabel(doc, 'Special Instructions', y);
        doc.setFillColor(254, 243, 199);
        doc.roundedRect(14, y, pageW - 28, 40, 3, 3, 'F');
        doc.setFont('helvetica', 'normal'); doc.setFontSize(9); doc.setTextColor(...DARK_COLOR);
        doc.text(String(prescription.special_instructions), 20, y + 14, { maxWidth: pageW - 40 });
        y += 50;
    }

    /* ── Signature area ── */
    y += 20;
    doc.setDrawColor(200, 200, 200);
    doc.line(pageW - 220, y, pageW - 14, y);
    doc.setFontSize(8); doc.setTextColor(107, 114, 128);
    doc.text('Prescribing Doctor Signature', pageW - 220, y + 12);

    _addFooter(doc);
    doc.save(`Prescription_${patient.patient_id || 'rx'}_${prescription.effective_date || 'latest'}.pdf`);
}


