// 6. PATIENT SUMMARY PDF (includes infection, consent, prescription)
// ─────────────────────────────────────────────────────────────────────────────
export function downloadPatientSummaryPDF(patient) {
    const doc = new jsPDF({ unit: 'pt', format: 'a4' });
    const now = new Date().toLocaleString('en-IN', {
        day: '2-digit', month: 'short', year: 'numeric',
        hour: '2-digit', minute: '2-digit'
    });

    _addHeader(doc, 'PATIENT SUMMARY', `Generated: ${now}`);

    const pageW = doc.internal.pageSize.getWidth();
    let y = 44;

    /* ── Infection Alert Banner ── */
    if (patient.is_infection_positive) {
        doc.setFillColor(254, 226, 226);
        doc.roundedRect(14, y, pageW - 28, 24, 5, 5, 'F');
        doc.setFont('helvetica', 'bold'); doc.setFontSize(10); doc.setTextColor(185, 28, 28);
        doc.text('INFECTION POSITIVE - REQUIRES MACHINE ISOLATION', pageW / 2, y + 16, { align: 'center' });
        y += 32;
    }

    /* ── Personal Info ── */
    y = _sectionLabel(doc, 'Personal Information', y);
    y = _table(doc, {
        startY: y,
        body: [
            ['Patient ID', patient.patient_id || 'N/A'],
            ['Name', patient.full_name || `${patient.first_name || ''} ${patient.last_name || ''}`.trim() || 'N/A'],
            ['Date of Birth', patient.date_of_birth || 'N/A'],
            ['Age', patient.age != null ? `${patient.age} years` : 'N/A'],
            ['Gender', patient.gender || 'N/A'],
            ['Blood Type', patient.blood_type || 'N/A'],
            ['Phone', patient.phone_number || 'N/A'],
            ['Address', patient.address || 'N/A'],
            ['Emergency Contact', `${patient.emergency_contact_name || 'N/A'} - ${patient.emergency_contact_phone || 'N/A'}`],
        ],
        columnStyles: { 0: { fontStyle: 'bold', cellWidth: 160, fillColor: LIGHT_GRAY } },
        bodyStyles: { fontSize: 8.5, cellPadding: 6 },
        alternateRowStyles: { fillColor: WHITE },
        margin: { left: 14, right: 14 },
        tableWidth: pageW - 28,
    }) + 12;

    /* ── Medical Info ── */
    y = _sectionLabel(doc, 'Medical Information', y);
    y = _table(doc, {
        startY: y,
        body: [
            ['Primary Diagnosis', patient.primary_diagnosis || 'N/A'],
            ['Comorbidities', patient.comorbidities || 'None'],
            ['Allergies', patient.allergies || 'None known'],
            ['Current Medications', patient.current_medications || 'None'],
            ['Dialysis Type', patient.dialysis_type || 'N/A'],
            ['Vascular Access', patient.vascular_access || 'N/A'],
            ['Dry Weight', patient.dry_weight ? `${patient.dry_weight} kg` : 'N/A'],
        ],
        columnStyles: { 0: { fontStyle: 'bold', cellWidth: 160, fillColor: LIGHT_GRAY } },
        bodyStyles: { fontSize: 8.5, cellPadding: 6 },
        alternateRowStyles: { fillColor: WHITE },
        margin: { left: 14, right: 14 },
        tableWidth: pageW - 28,
    }) + 12;

    /* ── Infection Status ── */
    y = _sectionLabel(doc, 'Infection Status', y);
    const hbColor = patient.hepatitis_b_status === 'positive' ? [220, 38, 38] : DARK_COLOR;
    const hcColor = patient.hepatitis_c_status === 'positive' ? [220, 38, 38] : DARK_COLOR;
    const hivColor = patient.hiv_status === 'positive' ? [220, 38, 38] : DARK_COLOR;
    y = _table(doc, {
        startY: y,
        head: [['Test', 'Status', 'Last Screened']],
        body: [
            ['Hepatitis B (HBsAg)', (patient.hepatitis_b_status || 'Unknown').toUpperCase(), patient.last_infection_screening_date || 'Not tested'],
            ['Hepatitis C (Anti-HCV)', (patient.hepatitis_c_status || 'Unknown').toUpperCase(), patient.last_infection_screening_date || 'Not tested'],
            ['HIV', (patient.hiv_status || 'Unknown').toUpperCase(), patient.last_infection_screening_date || 'Not tested'],
            ['Hep B Vaccinated', patient.hepatitis_b_vaccinated ? 'YES' : 'NO', '-'],
        ],
        headStyles: { fillColor: [107, 33, 168], textColor: WHITE, fontStyle: 'bold', fontSize: 9 },
        bodyStyles: { fontSize: 8.5, textColor: DARK_COLOR },
        alternateRowStyles: { fillColor: [250, 245, 255] },
        margin: { left: 14, right: 14 },
        tableWidth: pageW - 28,
        columnStyles: { 0: { fontStyle: 'bold', cellWidth: 200 } },
    }) + 12;

    /* ── Consent Status ── */
    y = _sectionLabel(doc, 'Consent Status', y);
    const consentStatus = patient.is_consent_valid ? 'VALID' : 'EXPIRED / MISSING';
    const consentColor = patient.is_consent_valid ? GREEN_COLOR : [220, 38, 38];
    y = _table(doc, {
        startY: y,
        body: [
            ['Consent Given', patient.consent_given ? 'YES' : 'NO'],
            ['Consent Date', patient.consent_date || 'N/A'],
            ['Consent Expiry', patient.consent_expiry_date || 'N/A'],
            ['Status', consentStatus],
        ],
        columnStyles: { 0: { fontStyle: 'bold', cellWidth: 160, fillColor: LIGHT_GRAY } },
        bodyStyles: { fontSize: 8.5, cellPadding: 6 },
        alternateRowStyles: { fillColor: WHITE },
        margin: { left: 14, right: 14 },
        tableWidth: pageW - 28,
    }) + 12;

    /* ── Active Prescription Summary ── */
    if (patient.active_prescription) {
        const rx = patient.active_prescription;
        y = _sectionLabel(doc, 'Active Prescription', y);
        y = _table(doc, {
            startY: y,
            body: [
                ['Frequency', rx.frequency || 'N/A'],
                ['Duration', rx.duration_minutes ? `${rx.duration_minutes} min` : 'N/A'],
                ['Blood Flow Rate', rx.blood_flow_rate ? `${rx.blood_flow_rate} ml/min` : 'N/A'],
                ['Dialyzer', rx.dialyzer_type || 'N/A'],
                ['Prescribed By', rx.prescribed_by || 'N/A'],
                ['Effective From', rx.effective_date || 'N/A'],
            ],
            columnStyles: { 0: { fontStyle: 'bold', cellWidth: 160, fillColor: [240, 253, 244] } },
            bodyStyles: { fontSize: 8.5, cellPadding: 6 },
            alternateRowStyles: { fillColor: WHITE },
            margin: { left: 14, right: 14 },
            tableWidth: pageW - 28,
        }) + 12;
    }

    _addFooter(doc);
    doc.save(`Patient_${patient.patient_id || 'summary'}.pdf`);
}
