// 5. LAB REPORT PDF
// ─────────────────────────────────────────────────────────────────────────────
export function downloadLabReportPDF(labResult, patient = {}) {
    const doc = new jsPDF({ unit: 'pt', format: 'a4' });
    const now = new Date().toLocaleString('en-IN', {
        day: '2-digit', month: 'short', year: 'numeric',
        hour: '2-digit', minute: '2-digit'
    });

    _addHeader(doc, 'LABORATORY REPORT', `Test Date: ${labResult.test_date || 'N/A'}`);

    const pageW = doc.internal.pageSize.getWidth();
    let y = 44;

    /* ── Critical Alert Banner ── */
    if (labResult.is_critical) {
        doc.setFillColor(254, 226, 226);
        doc.roundedRect(14, y, pageW - 28, 28, 5, 5, 'F');
        doc.setFont('helvetica', 'bold'); doc.setFontSize(11); doc.setTextColor(185, 28, 28);
        doc.text('CRITICAL VALUES DETECTED - URGENT ATTENTION REQUIRED', pageW / 2, y + 18, { align: 'center' });
        y += 38;
    }

    /* ── Patient Info ── */
    y = _sectionLabel(doc, 'Patient Information', y);
    y = _table(doc, {
        startY: y,
        body: [
            ['Patient Name', patient.name || `${patient.first_name || ''} ${patient.last_name || ''}`.trim() || 'N/A'],
            ['Patient ID', patient.patient_id || 'N/A'],
            ['Test Date', labResult.test_date || 'N/A'],
            ['Ordered By', labResult.ordered_by_name || 'N/A'],
        ],
        columnStyles: { 0: { fontStyle: 'bold', cellWidth: 160, fillColor: LIGHT_GRAY } },
        bodyStyles: { fontSize: 9, cellPadding: 6 },
        alternateRowStyles: { fillColor: WHITE },
        margin: { left: 14, right: 14 },
        tableWidth: pageW - 28,
    }) + 12;

    /* ── Dialysis Adequacy (THE MOST IMPORTANT SECTION) ── */
    y = _sectionLabel(doc, 'Dialysis Adequacy (Key Metrics)', y);
    const adequacyRows = [];
    if (labResult.pre_dialysis_bun != null) adequacyRows.push(['Pre-Dialysis BUN', `${labResult.pre_dialysis_bun} mg/dL`, '-']);
    if (labResult.post_dialysis_bun != null) adequacyRows.push(['Post-Dialysis BUN', `${labResult.post_dialysis_bun} mg/dL`, '-']);
    if (labResult.urr != null) {
        const urrStatus = Number(labResult.urr) >= 65 ? 'ADEQUATE' : 'INADEQUATE';
        adequacyRows.push(['URR (Urea Reduction Ratio)', `${labResult.urr}%`, `Target: >=65% [${urrStatus}]`]);
    }
    if (labResult.ktv_estimated != null) {
        const ktvStatus = Number(labResult.ktv_estimated) >= 1.2 ? 'ADEQUATE' : 'INADEQUATE';
        adequacyRows.push(['Kt/V (Dialysis Dose)', `${labResult.ktv_estimated}`, `Target: >=1.2 [${ktvStatus}]`]);
    }
    if (adequacyRows.length > 0) {
        y = _table(doc, {
            startY: y,
            head: [['Test', 'Result', 'Reference']],
            body: adequacyRows,
            headStyles: { fillColor: [124, 58, 237], textColor: WHITE, fontStyle: 'bold', fontSize: 9 },
            bodyStyles: { fontSize: 9, textColor: DARK_COLOR },
            alternateRowStyles: { fillColor: [245, 243, 255] },
            margin: { left: 14, right: 14 },
            tableWidth: pageW - 28,
            columnStyles: { 0: { fontStyle: 'bold', cellWidth: 200 } },
        }) + 12;
    }

    /* ── Kidney Function ── */
    const kftRows = [];
    if (labResult.blood_urea_nitrogen != null) kftRows.push(['BUN', `${labResult.blood_urea_nitrogen} mg/dL`, '7-20 mg/dL']);
    if (labResult.serum_creatinine != null) kftRows.push(['Serum Creatinine', `${labResult.serum_creatinine} mg/dL`, '0.6-1.2 mg/dL']);
    if (kftRows.length > 0) {
        y = _sectionLabel(doc, 'Kidney Function Tests', y);
        y = _table(doc, {
            startY: y,
            head: [['Test', 'Result', 'Normal Range']],
            body: kftRows,
            headStyles: { fillColor: BRAND_COLOR, textColor: WHITE, fontStyle: 'bold', fontSize: 9 },
            bodyStyles: { fontSize: 8.5, textColor: DARK_COLOR },
            alternateRowStyles: { fillColor: [239, 246, 255] },
            margin: { left: 14, right: 14 },
            tableWidth: pageW - 28,
            columnStyles: { 0: { fontStyle: 'bold', cellWidth: 200 } },
        }) + 12;
    }

    /* ── CBC ── */
    const cbcRows = [];
    if (labResult.hemoglobin != null) {
        const hbFlag = Number(labResult.hemoglobin) < 8 ? ' ** CRITICAL LOW **' : '';
        cbcRows.push(['Hemoglobin', `${labResult.hemoglobin} g/dL${hbFlag}`, '10-12 g/dL (dialysis)']);
    }
    if (labResult.hematocrit != null) cbcRows.push(['Hematocrit', `${labResult.hematocrit}%`, '30-36%']);
    if (labResult.wbc_count != null) cbcRows.push(['WBC Count', `${labResult.wbc_count} x10^3/uL`, '4.5-11.0']);
    if (labResult.platelet_count != null) cbcRows.push(['Platelet Count', `${labResult.platelet_count} x10^3/uL`, '150-400']);
    if (cbcRows.length > 0) {
        y = _sectionLabel(doc, 'Complete Blood Count (CBC)', y);
        y = _table(doc, {
            startY: y,
            head: [['Test', 'Result', 'Normal Range']],
            body: cbcRows,
            headStyles: { fillColor: [220, 38, 38], textColor: WHITE, fontStyle: 'bold', fontSize: 9 },
            bodyStyles: { fontSize: 8.5, textColor: DARK_COLOR },
            alternateRowStyles: { fillColor: [254, 242, 242] },
            margin: { left: 14, right: 14 },
            tableWidth: pageW - 28,
            columnStyles: { 0: { fontStyle: 'bold', cellWidth: 200 } },
        }) + 12;
    }

    /* ── Electrolytes ── */
    const electroRows = [];
    if (labResult.serum_sodium != null) electroRows.push(['Sodium (Na+)', `${labResult.serum_sodium} mEq/L`, '135-145']);
    if (labResult.serum_potassium != null) {
        const kFlag = Number(labResult.serum_potassium) > 6.0 ? ' ** CRITICAL HIGH **' : '';
        electroRows.push(['Potassium (K+)', `${labResult.serum_potassium} mEq/L${kFlag}`, '3.5-5.0']);
    }
    if (labResult.serum_calcium != null) electroRows.push(['Calcium (Ca2+)', `${labResult.serum_calcium} mg/dL`, '8.5-10.5']);
    if (labResult.serum_phosphorus != null) electroRows.push(['Phosphorus', `${labResult.serum_phosphorus} mg/dL`, '2.5-4.5']);
    if (labResult.serum_bicarbonate != null) electroRows.push(['Bicarbonate', `${labResult.serum_bicarbonate} mEq/L`, '22-26']);
    if (electroRows.length > 0) {
        y = _sectionLabel(doc, 'Electrolytes', y);
        y = _table(doc, {
            startY: y,
            head: [['Test', 'Result', 'Normal Range']],
            body: electroRows,
            headStyles: { fillColor: [234, 179, 8], textColor: DARK_COLOR, fontStyle: 'bold', fontSize: 9 },
            bodyStyles: { fontSize: 8.5, textColor: DARK_COLOR },
            alternateRowStyles: { fillColor: [254, 252, 232] },
            margin: { left: 14, right: 14 },
            tableWidth: pageW - 28,
            columnStyles: { 0: { fontStyle: 'bold', cellWidth: 200 } },
        }) + 12;
    }

    /* ── Infection Screening ── */
    const infRows = [];
    if (labResult.hbsag) infRows.push(['HBsAg (Hepatitis B)', labResult.hbsag, 'Non-Reactive']);
    if (labResult.anti_hcv) infRows.push(['Anti-HCV (Hepatitis C)', labResult.anti_hcv, 'Non-Reactive']);
    if (labResult.hiv) infRows.push(['HIV', labResult.hiv, 'Non-Reactive']);
    if (infRows.length > 0) {
        y = _sectionLabel(doc, 'Infection Screening', y);
        y = _table(doc, {
            startY: y,
            head: [['Test', 'Result', 'Expected']],
            body: infRows,
            headStyles: { fillColor: [107, 33, 168], textColor: WHITE, fontStyle: 'bold', fontSize: 9 },
            bodyStyles: { fontSize: 8.5, textColor: DARK_COLOR },
            alternateRowStyles: { fillColor: [250, 245, 255] },
            margin: { left: 14, right: 14 },
            tableWidth: pageW - 28,
            columnStyles: { 0: { fontStyle: 'bold', cellWidth: 200 } },
        }) + 12;
    }

    _addFooter(doc);
    doc.save(`LabReport_${patient.patient_id || 'patient'}_${labResult.test_date || 'latest'}.pdf`);
}


