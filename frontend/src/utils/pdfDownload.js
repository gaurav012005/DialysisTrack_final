/**
 * pdfDownload.js — Client-side PDF generation utilities
 * Uses jsPDF + jspdf-autotable to generate professional PDFs in the browser.
 *
 * IMPORTANT: Use named import for autoTable to avoid Vite HMR issues.
 * Call autoTable(doc, options) — NOT doc.autoTable(options).
 */

import jsPDF from 'jspdf';
import autoTable from 'jspdf-autotable'; // named import — patches via function call

const BRAND_COLOR  = [14, 116, 144];   // cyan-700
const GREEN_COLOR  = [5, 150, 105];    // emerald-600
const DARK_COLOR   = [31, 41, 55];     // gray-800
const LIGHT_GRAY   = [243, 244, 246];  // gray-100
const WHITE        = [255, 255, 255];

/** Format currency safely — ₹ glyph not in default jsPDF font */
function cur(amount) {
    return `Rs. ${Number(amount || 0).toFixed(2)}`;
}

/* ─── Shared header ─────────────────────────────────────────────────────── */
function _addHeader(doc, title, subtitle = '') {
    const pageW = doc.internal.pageSize.getWidth();
    doc.setFillColor(...BRAND_COLOR);
    doc.rect(0, 0, pageW, 30, 'F');

    doc.setTextColor(...WHITE);
    doc.setFontSize(18);
    doc.setFont('helvetica', 'bold');
    doc.text('DialysisTrack', 14, 12);

    doc.setFontSize(8);
    doc.setFont('helvetica', 'normal');
    doc.text('Dialysis Management System', 14, 21);

    doc.setFontSize(13);
    doc.setFont('helvetica', 'bold');
    doc.text(title, pageW - 14, 13, { align: 'right' });

    if (subtitle) {
        doc.setFontSize(8);
        doc.setFont('helvetica', 'normal');
        doc.text(subtitle, pageW - 14, 22, { align: 'right' });
    }

    doc.setDrawColor(...GREEN_COLOR);
    doc.setLineWidth(1.5);
    doc.line(0, 30, pageW, 30);
}

/* ─── Shared footer ─────────────────────────────────────────────────────── */
function _addFooter(doc) {
    const pageH   = doc.internal.pageSize.getHeight();
    const pageW   = doc.internal.pageSize.getWidth();
    const total   = doc.internal.getNumberOfPages();
    for (let i = 1; i <= total; i++) {
        doc.setPage(i);
        doc.setDrawColor(229, 231, 235);
        doc.setLineWidth(0.5);
        doc.line(14, pageH - 14, pageW - 14, pageH - 14);
        doc.setFontSize(7.5);
        doc.setTextColor(156, 163, 175);
        doc.text('Generated by DialysisTrack System', 14, pageH - 6);
        doc.text(`Page ${i} of ${total}`, pageW - 14, pageH - 6, { align: 'right' });
    }
}

/* ─── Section label helper ─────────────────────────────────────────────── */
function _sectionLabel(doc, label, y) {
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(10.5);
    doc.setTextColor(...BRAND_COLOR);
    doc.text(label, 14, y);
    return y + 6;
}

/* ─── Shorthand: run autoTable and return finalY ───────────────────────── */
function _table(doc, opts) {
    autoTable(doc, opts);
    return doc.lastAutoTable.finalY;
}


// ─────────────────────────────────────────────────────────────────────────────
// 1. PAYMENT RECEIPT PDF
// ─────────────────────────────────────────────────────────────────────────────
export function downloadReceiptPDF(bill, patient = {}) {
    const doc = new jsPDF({ unit: 'pt', format: 'a4' });
    const now = new Date().toLocaleString('en-IN', {
        day: '2-digit', month: 'short', year: 'numeric',
        hour: '2-digit', minute: '2-digit'
    });

    _addHeader(doc, 'PAYMENT RECEIPT', `Generated: ${now}`);

    const pageW = doc.internal.pageSize.getWidth();
    let y = 40;

    /* ── Patient + Bill info cards ── */
    const halfW = (pageW - 28) / 2 - 4;

    doc.setFillColor(...LIGHT_GRAY);
    doc.roundedRect(14, y, halfW, 68, 3, 3, 'F');
    doc.setFillColor(230, 247, 255);
    doc.roundedRect(18 + halfW, y, halfW, 68, 3, 3, 'F');

    // Patient block
    doc.setFont('helvetica', 'bold'); doc.setFontSize(9); doc.setTextColor(...BRAND_COLOR);
    doc.text('PATIENT', 20, y + 12);
    doc.setFont('helvetica', 'normal'); doc.setTextColor(...DARK_COLOR); doc.setFontSize(8.5);
    doc.text(`Name    : ${patient.name || 'N/A'}`,       20, y + 26);
    doc.text(`ID      : ${patient.patient_id || 'N/A'}`, 20, y + 39);
    doc.text(`Phone   : ${patient.phone || 'N/A'}`,      20, y + 52);

    // Bill block
    const rx = 22 + halfW;
    doc.setFont('helvetica', 'bold'); doc.setFontSize(9); doc.setTextColor(...BRAND_COLOR);
    doc.text('BILL', rx, y + 12);
    doc.setFont('helvetica', 'normal'); doc.setTextColor(...DARK_COLOR); doc.setFontSize(8.5);
    doc.text(`Bill #  : ${bill.bill_number || bill.id || 'N/A'}`,         rx, y + 26);
    doc.text(`Date    : ${bill.bill_date || bill.created_at || now}`,      rx, y + 39);
    doc.text(`Status  : ${(bill.payment_status || bill.status || 'N/A').toUpperCase()}`, rx, y + 52);

    y += 80;

    /* ── Charges table ── */
    y = _sectionLabel(doc, 'Charges Breakdown', y);
    const chargesRows = [];
    if (bill.session_cost     !== undefined) chargesRows.push(['Dialysis Session',  cur(bill.session_cost)]);
    if (bill.medicines_cost   !== undefined) chargesRows.push(['Medicines',         cur(bill.medicines_cost)]);
    if (bill.consultation_fee !== undefined) chargesRows.push(['Consultation Fee',  cur(bill.consultation_fee)]);
    if (bill.other_charges    !== undefined) chargesRows.push(['Other Charges',     cur(bill.other_charges)]);
    if (chargesRows.length === 0)             chargesRows.push(['Services',          cur(bill.total_amount)]);
    if (bill.subtotal !== undefined)          chargesRows.push(['Subtotal',          cur(bill.subtotal)]);
    if (bill.gst_amount !== undefined)        chargesRows.push(['GST (18%)',         cur(bill.gst_amount)]);
    if (Number(bill.discount) > 0)            chargesRows.push(['Discount',          `- ${cur(bill.discount)}`]);

    y = _table(doc, {
        startY: y,
        head: [['Description', 'Amount']],
        body: chargesRows,
        columnStyles: { 1: { halign: 'right' } },
        headStyles: { fillColor: BRAND_COLOR, textColor: WHITE, fontStyle: 'bold', fontSize: 9 },
        bodyStyles: { fontSize: 8.5, textColor: DARK_COLOR },
        alternateRowStyles: { fillColor: [249, 250, 251] },
        margin: { left: 14, right: 14 },
        tableWidth: pageW - 28,
    }) + 10;

    /* ── Summary box ── */
    const summaryRows = [
        ['Total Amount',  cur(bill.total_amount)],
        ['Paid Amount',   cur(bill.paid_amount)],
        [Number(bill.balance_amount || 0) > 0 ? 'Balance Due' : 'Fully Paid', cur(bill.balance_amount)],
    ];
    y = _table(doc, {
        startY: y,
        body: summaryRows,
        columnStyles: {
            0: { fontStyle: 'bold', textColor: DARK_COLOR },
            1: { halign: 'right', fontStyle: 'bold' },
        },
        bodyStyles: { fontSize: 9.5 },
        alternateRowStyles: { fillColor: WHITE },
        margin: { left: pageW - 220, right: 14 },
        tableWidth: 210,
    }) + 14;

    /* ── Payment history ── */
    const payments = bill.payments || [];
    if (payments.length > 0) {
        y = _sectionLabel(doc, 'Payment History', y);
        y = _table(doc, {
            startY: y,
            head: [['Date', 'Method', 'Amount', 'Transaction ID']],
            body: payments.map(p => [
                p.payment_date || 'N/A',
                (p.payment_mode || p.payment_method || 'N/A').toUpperCase(),
                cur(p.amount),
                p.transaction_id || '—',
            ]),
            headStyles: { fillColor: GREEN_COLOR, textColor: WHITE, fontStyle: 'bold', fontSize: 8.5 },
            bodyStyles: { fontSize: 8, textColor: DARK_COLOR },
            alternateRowStyles: { fillColor: [240, 253, 244] },
            margin: { left: 14, right: 14 },
            tableWidth: pageW - 28,
            columnStyles: { 2: { halign: 'right' } },
        }) + 10;
    }

    /* ── PAID watermark ── */
    if (Number(bill.balance_amount || 0) === 0) {
        doc.setFontSize(60);
        doc.setFont('helvetica', 'bold');
        doc.setTextColor(5, 150, 105);
        try {
            doc.setGState(new doc.GState({ opacity: 0.08 }));
        } catch (_) { /* GState unsupported — skip opacity */ }
        doc.text('PAID', pageW / 2, 500, { align: 'center', angle: -30 });
        try { doc.setGState(new doc.GState({ opacity: 1 })); } catch (_) {}
    }

    _addFooter(doc);
    doc.save(`Receipt_${bill.bill_number || bill.id || 'bill'}.pdf`);
}


// ─────────────────────────────────────────────────────────────────────────────
// 2. SESSION DETAILS PDF
// ─────────────────────────────────────────────────────────────────────────────
export function downloadSessionPDF(session, patient = {}) {
    const doc = new jsPDF({ unit: 'pt', format: 'a4' });
    const now = new Date().toLocaleString('en-IN', {
        day: '2-digit', month: 'short', year: 'numeric',
        hour: '2-digit', minute: '2-digit'
    });
    const sessionDate = session.date
        ? new Date(session.date).toLocaleDateString('en-IN', {
            day: '2-digit', month: 'short', year: 'numeric'
          })
        : 'N/A';

    _addHeader(doc, 'SESSION SUMMARY', `Session: ${sessionDate}`);

    const pageW = doc.internal.pageSize.getWidth();
    let y = 40;

    /* ── Patient info banner ── */
    doc.setFillColor(...LIGHT_GRAY);
    doc.roundedRect(14, y, pageW - 28, 58, 3, 3, 'F');
    doc.setFont('helvetica', 'bold'); doc.setFontSize(9); doc.setTextColor(...BRAND_COLOR);
    doc.text('PATIENT INFORMATION', 20, y + 13);
    doc.setFont('helvetica', 'normal'); doc.setTextColor(...DARK_COLOR); doc.setFontSize(8.5);
    doc.text(`Name       : ${patient.name || 'N/A'}`,           20, y + 27);
    doc.text(`Patient ID : ${patient.patient_id || 'N/A'}`,     20, y + 40);
    doc.text(`Blood Type : ${patient.blood_type || 'N/A'}`,     20, y + 53);

    const col2 = pageW / 2 + 10;
    doc.text(`Session ID : #${session.id || 'N/A'}`,            col2, y + 27);
    doc.text(`Machine    : ${session.machine_number || 'N/A'}`, col2, y + 40);
    doc.text(`Duration   : ${session.duration_minutes || session.duration || 'N/A'} min`, col2, y + 53);
    y += 70;

    /* ── Vitals comparison ── */
    y = _sectionLabel(doc, 'Vital Signs (Pre vs Post)', y);
    y = _table(doc, {
        startY: y,
        head: [['Parameter', 'Pre-Dialysis', 'Post-Dialysis', 'Change']],
        body: [
            [
                'Weight (kg)',
                String(session.pre_dialysis_weight  || session.pre_weight  || 'N/A'),
                String(session.post_dialysis_weight || session.post_weight || 'N/A'),
                (session.pre_dialysis_weight && session.post_dialysis_weight)
                    ? `${(Number(session.pre_dialysis_weight) - Number(session.post_dialysis_weight)).toFixed(2)} kg lost`
                    : '—',
            ],
            [
                'Blood Pressure (mmHg)',
                session.pre_bp_systolic  ? `${session.pre_bp_systolic}/${session.pre_bp_diastolic}`   : 'N/A',
                session.post_bp_systolic ? `${session.post_bp_systolic}/${session.post_bp_diastolic}` : 'N/A',
                '—',
            ],
            ['Heart Rate (bpm)',    String(session.pre_heart_rate  || 'N/A'), String(session.post_heart_rate  || 'N/A'), '—'],
            ['Temperature (deg F)', String(session.pre_temperature || 'N/A'), String(session.post_temperature || 'N/A'), '—'],
        ],
        headStyles: { fillColor: BRAND_COLOR, textColor: WHITE, fontStyle: 'bold', fontSize: 9 },
        bodyStyles: { fontSize: 8.5, textColor: DARK_COLOR },
        alternateRowStyles: { fillColor: [239, 246, 255] },
        margin: { left: 14, right: 14 },
        tableWidth: pageW - 28,
        columnStyles: {
            0: { fontStyle: 'bold', cellWidth: 140 },
            3: { textColor: [5, 150, 105], fontStyle: 'italic' },
        },
    }) + 14;

    /* ── Parameters ── */
    y = _sectionLabel(doc, 'Dialysis Parameters', y);
    y = _table(doc, {
        startY: y,
        head: [['Parameter', 'Value']],
        body: [
            ['Session Duration',         `${session.duration_minutes || session.duration || 'N/A'} minutes`],
            ['Ultrafiltration Volume',    `${session.ultrafiltration_volume || 'N/A'} L`],
            ['Machine Number',             session.machine_number || 'N/A'],
            ['Session Date',               sessionDate],
            ['Session Time',               session.time
                ? new Date(`1970-01-01T${session.time}`).toLocaleTimeString('en-IN', { hour: '2-digit', minute: '2-digit' })
                : 'N/A'],
        ],
        headStyles: { fillColor: GREEN_COLOR, textColor: WHITE, fontStyle: 'bold', fontSize: 9 },
        bodyStyles: { fontSize: 8.5, textColor: DARK_COLOR },
        alternateRowStyles: { fillColor: [240, 253, 244] },
        margin: { left: 14, right: 14 },
        tableWidth: pageW - 28,
        columnStyles: { 0: { fontStyle: 'bold', cellWidth: 200 } },
    }) + 14;

    /* ── Complications / Notes ── */
    const hasComps = session.complications && String(session.complications).trim();
    const hasNotes = session.notes        && String(session.notes).trim();
    if (hasComps || hasNotes) {
        y = _sectionLabel(doc, 'Clinical Notes', y) + 4;
        if (hasComps) {
            doc.setFillColor(254, 243, 199);
            doc.roundedRect(14, y, pageW - 28, 28, 3, 3, 'F');
            doc.setFont('helvetica', 'bold'); doc.setFontSize(8.5); doc.setTextColor(146, 64, 14);
            doc.text('Complications:', 20, y + 11);
            doc.setFont('helvetica', 'normal'); doc.setTextColor(...DARK_COLOR);
            doc.text(String(session.complications), 110, y + 11, { maxWidth: pageW - 130 });
            y += 34;
        }
        if (hasNotes) {
            doc.setFillColor(239, 246, 255);
            doc.roundedRect(14, y, pageW - 28, 28, 3, 3, 'F');
            doc.setFont('helvetica', 'bold'); doc.setFontSize(8.5); doc.setTextColor(30, 64, 175);
            doc.text('Notes:', 20, y + 11);
            doc.setFont('helvetica', 'normal'); doc.setTextColor(...DARK_COLOR);
            doc.text(String(session.notes), 70, y + 11, { maxWidth: pageW - 90 });
            y += 34;
        }
    }

    doc.setFontSize(7.5);
    doc.setTextColor(156, 163, 175);
    doc.text(`Report generated on ${now}`, 14, y + 14);

    _addFooter(doc);
    doc.save(`Session_${session.id || 'summary'}_${sessionDate.replace(/ /g, '-')}.pdf`);
}


